#!/usr/bin/env node
/**
 * Regenerate the artifacts this repo vendors from the zeph-to/plugin repo:
 *
 *   src/zeph-core.generated.ts   ← plugin scripts/extract-core.js --json
 *                                  (manifest-driven slices of CORE_RULES.md)
 *   src/fixtures/gate-vectors.json ← plugin tests/fixtures/gate-vectors.json
 *                                  (canonical push-gate parity vectors)
 *
 * Usage:
 *   node scripts/sync-from-plugin.mjs [--plugin <path>]          # regenerate
 *   node scripts/sync-from-plugin.mjs --check [--plugin <path>]  # CI drift check
 *
 * The plugin checkout defaults to the sibling `../plugin`; CI checks out
 * zeph-to/plugin at `./plugin` and passes --plugin ./plugin.
 */
import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const args = process.argv.slice(2);
const checkMode = args.includes('--check');
const pluginFlag = args.indexOf('--plugin');
const pluginRoot = resolve(pluginFlag !== -1 ? args[pluginFlag + 1] : join(REPO_ROOT, '..', 'plugin'));

const fail = (msg) => {
    console.error(`❌ sync-from-plugin: ${msg}`);
    process.exit(1);
};

const extractorPath = join(pluginRoot, 'scripts', 'extract-core.js');
const vectorsSrcPath = join(pluginRoot, 'tests', 'fixtures', 'gate-vectors.json');
if (!existsSync(extractorPath)) {
    fail(`plugin checkout not found at ${pluginRoot} (missing scripts/extract-core.js).\n` +
        '   Clone zeph-to/plugin as a sibling directory, or pass --plugin <path>.');
}
if (!existsSync(vectorsSrcPath)) fail(`missing ${vectorsSrcPath}`);

let extracted;
try {
    extracted = JSON.parse(execFileSync('node', [extractorPath, '--json'], { encoding: 'utf-8' }));
} catch (err) {
    fail(`plugin extractor failed: ${err.message}`);
}

// JSON.stringify-serialized string consts sidestep template-literal escaping
// (the rule text is full of backticks and ${}-looking snippets).
const generatedTs = `// GENERATED by scripts/sync-from-plugin.mjs — DO NOT EDIT.
// Source of truth: zeph-to/plugin docs/CORE_RULES.md, sliced per
// scripts/core-rules.manifest.json. Regenerate with \`npm run sync:plugin\`
// (requires a zeph-to/plugin checkout, sibling ../plugin by default).
// CI cross-checks this file against plugin main and fails on drift.

/** sha256 over the plugin manifest + extracted rule text at generation time. */
export const ZEPH_CORE_SOURCE_HASH = ${JSON.stringify(extracted.sourceHash)};

/** Shared rule core for agents with a Stop-equivalent hook (Cursor, Windsurf, Gemini, Codex, Copilot). */
export const ZEPH_CORE_HOOK_DRIVEN = ${JSON.stringify(extracted.hookDriven)};

/** Shared rule core for rule-only agents without a Stop hook (Cline, Aider). */
export const ZEPH_CORE_RULE_ONLY = ${JSON.stringify(extracted.ruleOnly)};
`;

const vectorsJson = readFileSync(vectorsSrcPath, 'utf-8');

const targets = [
    { path: join(REPO_ROOT, 'src', 'zeph-core.generated.ts'), content: generatedTs, label: 'src/zeph-core.generated.ts' },
    { path: join(REPO_ROOT, 'src', 'fixtures', 'gate-vectors.json'), content: vectorsJson, label: 'src/fixtures/gate-vectors.json' },
];

if (checkMode) {
    const drifted = targets.filter((t) => {
        try {
            return readFileSync(t.path, 'utf-8') !== t.content;
        } catch {
            return true; // missing counts as drift
        }
    });
    if (drifted.length > 0) {
        fail(`drift detected in: ${drifted.map((t) => t.label).join(', ')}\n` +
            '   Run `npm run sync:plugin` against current plugin main and commit the result.');
    }
    console.log('✓ sync-from-plugin: generated artifacts match the plugin repo.');
    process.exit(0);
}

for (const t of targets) {
    mkdirSync(dirname(t.path), { recursive: true });
    writeFileSync(t.path, t.content);
    console.log(`✓ wrote ${t.label}`);
}
console.log(`✓ sync-from-plugin: done (source hash ${extracted.sourceHash.slice(0, 12)}…)`);
